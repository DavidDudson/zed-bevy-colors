#[cfg(test)]
#[allow(clippy::unwrap_used, clippy::expect_used, clippy::panic)]
mod tests {
    use super::*;

    #[test]
    fn css_tomato() {
        let c = css_named("TOMATO").unwrap();
        assert_eq!(c, Rgba::from_u8(255, 99, 71, 255));
    }

    #[test]
    fn tailwind_blue_500() {
        let c = tailwind_named("BLUE_500").unwrap();
        let expected = parse_hex("3b82f6").unwrap();
        assert_eq!(c, expected);
    }

    #[test]
    fn tailwind_unknown() {
        assert!(tailwind_named("BLUE_42").is_none());
    }

    #[test]
    fn lookup_palette_dispatch() {
        assert!(lookup_palette("css", "TOMATO").is_some());
        assert!(lookup_palette("tailwind", "RED_500").is_some());
        assert!(lookup_palette("basic", "RED").is_some());
        assert!(lookup_palette("nope", "RED").is_none());
    }
}
